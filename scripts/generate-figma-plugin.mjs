import { readFileSync, writeFileSync, mkdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const screenshotsDir = join(__dirname, '../screenshots')
const pluginDir = join(__dirname, '../figma-plugin')
mkdirSync(pluginDir, { recursive: true })

const screens = [
  '01-intro',
  '02-intent',
  '03-record',
  '04-review',
  '05-location',
  '06-consent',
  '07-upload',
  '08-result',
  '09-archive',
]

const imageData = screens.map(name => ({
  name,
  base64: readFileSync(join(screenshotsDir, `${name}.png`)).toString('base64'),
}))

const pluginCode = `
// Auto-generated by generate-figma-plugin.mjs
const screens = ${JSON.stringify(imageData)};

async function main() {
  const page = figma.currentPage;
  const FRAME_W = 390;
  const FRAME_H = 844;
  const GAP = 32;

  for (let i = 0; i < screens.length; i++) {
    const { name, base64 } = screens[i];

    // Decode base64 → Uint8Array
    const bin = atob(base64);
    const bytes = new Uint8Array(bin.length);
    for (let j = 0; j < bin.length; j++) bytes[j] = bin.charCodeAt(j);

    const imageHash = figma.createImage(bytes).hash;

    const frame = figma.createFrame();
    frame.resize(FRAME_W, FRAME_H);
    frame.x = i * (FRAME_W + GAP);
    frame.y = 0;
    frame.name = name;
    frame.fills = [{ type: 'IMAGE', scaleMode: 'FILL', imageHash }];

    page.appendChild(frame);
  }

  figma.viewport.scrollAndZoomIntoView(page.children);
  figma.closePlugin('Done! Created ' + screens.length + ' frames.');
}

main().catch(err => figma.closePlugin('Error: ' + err.message));
`.trim()

writeFileSync(join(pluginDir, 'code.js'), pluginCode)
writeFileSync(join(pluginDir, 'manifest.json'), JSON.stringify({
  name: 'Kadim Screen Push',
  id: 'kadim-screen-push-local',
  api: '1.0.0',
  main: 'code.js',
  editorType: ['figma'],
}, null, 2))

console.log('Plugin generated in figma-plugin/')
console.log('')
console.log('To run:')
console.log('  1. Open Figma Desktop → your file')
console.log('  2. Main menu → Plugins → Development → Import plugin from manifest…')
console.log('  3. Select figma-plugin/manifest.json')
console.log('  4. Run it (Plugins → Development → Kadim Screen Push)')
